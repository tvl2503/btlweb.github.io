package fe;

/**
 *
 * @author Linh Tran Vo
 */
public class NavBar {
    private String name, path;
    public NavBar() {
    }
    public NavBar(String name, String path) {
        this.name = name;
        this.path = path;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
    
   
    
}
